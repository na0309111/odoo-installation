#--------------------------------------------------
# Update Server
#--------------------------------------------------
echo -e "\n---- Update Server to make sure all up to date --------------------------------------------------------------------------------"
sudo apt-get update -y
sudo apt-get dist-upgrade -y
#--------------------------------------------------
# Install pip
#--------------------------------------------------
echo -e "\n---- Install pip & pip essential --------------------------------------------------------------------------------"
sudo apt-get install -y python-pip python-dev build-essential
echo -e "\n---- Upgrade pip to the lasted version --------------------------------------------------------------------------------"
sudo pip install --upgrade pip
#--------------------------------------------------
# install simplejson
#--------------------------------------------------
echo -e "\n---- Install simplejson  --------------------------------------------------------------------------------"
pip install simplejson
#--------------------------------------------------
# Install oauthlib
#--------------------------------------------------
echo -e "\n---- Install oauthlib --------------------------------------------------------------------------------"
pip install oauthlib
#--------------------------------------------------
# requests
#--------------------------------------------------
echo -e "\n---- Install requests upgrade --------------------------------------------------------------------------------"
pip install requests --upgrade
#--------------------------------------------------
# Install boto
#--------------------------------------------------
echo -e "\n---- Install boto --------------------------------------------------------------------------------"
pip install boto
#--------------------------------------------------
# install pysftp
#--------------------------------------------------
echo -e "\n---- Install pysftp --------------------------------------------------------------------------------"
pip install pysftp
#--------------------------------------------------
# install git if not install yet.
#--------------------------------------------------
echo -e "\n---- install git if not install yet --------------------------------------------------------------------------------"
sudo apt-get install -y git
#--------------------------------------------------
# Re-update Server
#--------------------------------------------------
echo -e "\n---- Update & Upgrade Server --------------------------------------------------------------------------------"
sudo apt-get update -y
sudo apt-get dist-upgrade -y
#--------------------------------------------------
# get responsitories
#--------------------------------------------------
echo -e "\n---- Clone saas responsitories --------------------------------------------------------------------------------"
cd /home/
mkdir odoo
cd /home/odoo
git clone -b 10.0 https://github.com/it-projects-llc/odoo-saas-tools.git
git clone -b 10.0 https://github.com/it-projects-llc/e-commerce.git
git clone -b 10.0 https://github.com/it-projects-llc/access-addons.git
#--------------------------------------------------
# install odoo
